    private void jTextField6FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField6FocusLost
        try {
            if (jTextField5.getText().isEmpty()) {
                //
            } else {
                DecimalFormat df = new DecimalFormat("00.00");
                double up = Double.parseDouble(jTextField6.getText().trim());
                int qty = Integer.parseInt(jTextField3.getText().trim());
                double total = up * qty;
                jTextField5.setText(df.format(total));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField6FocusLost

    private void jTextField6KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField6KeyReleased
        try {
            char c = evt.getKeyChar();
            if (Character.isLetter(c)) {
                evt.consume();
                Runtime r = Runtime.getRuntime();
                JOptionPane.showMessageDialog(rootPane, "You can't enter letters here", "Input Error", JOptionPane.ERROR_MESSAGE);
                jTextField6.setText(null);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTextField6KeyReleased

