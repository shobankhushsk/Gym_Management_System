    private void jTextField1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1FocusLost
        try {
            if (jTextField1.getText().isEmpty()) {
                //do nothing
            } else {
                ResultSet rs1 = JDBC.getdata("SELECT * FROM item_registration WHERE item_id='" + jTextField1.getText().trim() + "'");
                if (rs1.next()) {
                    ResultSet rs2 = JDBC.getdata("SELECT * FROM item_registration WHERE item_id='" + jTextField1.getText().trim() + "'");
                    while (rs2.next()) {
                        jTextField2.setText(rs1.getString("description"));
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Item code does not match", "Input error", JOptionPane.ERROR_MESSAGE);
                    jTextField1.setText("");
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTextField1FocusLost

