    void toatalAmount() {
        try {
            DecimalFormat df = new DecimalFormat("00.00");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            double gross_amount = 0;
            for (int x = 0; x < dtm.getRowCount(); x++) {
                gross_amount = gross_amount + Double.parseDouble(jTable1.getValueAt(x, 3).toString());
            }
            jTextField4.setText(df.format(gross_amount));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    void printInvoice() {
        try {
            JasperReport r = JasperCompileManager.compileReport(Config.invoicePath);
            HashMap para = new HashMap();
            para.put("ino", jTextField8.getText().trim());
            para.put("total", jTextField4.getText().trim());
            para.put("paid", jTextField5.getText().trim());
            para.put("balance", jTextField7.getText().trim());
            JasperPrint jp = JasperFillManager.fillReport(r, para, new JRTableModelDataSource(jTable1.getModel()));
            JasperViewer.viewReport(jp, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    void invoiceNo() {
        try {
            ResultSet rs = JDBC.getdata("SELECT MAX(ino) FROM invoice");
            while (rs.next()) {
                String batch1 = rs.getString("MAX(ino)");
                int batch2 = Integer.parseInt(batch1.substring(2));
                int batch3 = (batch2 + 1);
                jTextField8.setText("IN" + batch3);
            }
        } catch (Exception e) {
            jTextField8.setText("IN" + 15000001);
        }
    }

