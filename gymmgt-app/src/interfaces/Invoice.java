    private void jTextField5FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField5FocusLost
        try {
            if (jTextField5.getText().isEmpty()) {
                //
            } else {
                DecimalFormat df = new DecimalFormat("00.00");
                double total = Double.parseDouble(jTextField4.getText().trim());
                double paid = Double.parseDouble(jTextField5.getText().trim());
                if (paid <= 0) {
                    JOptionPane.showMessageDialog(null, "You cant enter minus or negative values", "Input error", JOptionPane.ERROR_MESSAGE);
                    jTextField5.setText("");
                } else {
                    double balance = paid - total;
                    jTextField7.setText(df.format(balance));
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField5FocusLost

