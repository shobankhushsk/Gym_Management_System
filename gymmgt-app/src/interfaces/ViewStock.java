    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            if (jTextField1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Please enter item code", "Input error", JOptionPane.ERROR_MESSAGE);
            } else {
                ResultSet rs = JDBC.getdata("SELECT * FROM stock WHERE item_id='" + jTextField1.getText().trim() + "'");
                if (rs.next()) {
                    ResultSet rs1 = JDBC.getdata("SELECT * FROM stock WHERE item_id='" + jTextField1.getText().trim() + "'");
                    while (rs1.next()) {
                        Vector v = new Vector();
                        v.add(rs.getString("item_id"));
                        v.add(rs.getString("description"));
                        v.add(rs.getString("qty"));
                        dtm.addRow(v);
                    }
                    jTextField1.setText("");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Item code does not match", "", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed
